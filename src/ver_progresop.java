    public ver_progresop() {
        initComponents();
        
        
        this.setTitle("consulta de los progresos de las tareas");
        this.setLocation(335,200);
        this.setResizable(false);
        
        try {
            
            String url = "jdbc:mysql://localhost:3306/proyecto";
            String usuario = "root";
            String contraseña = "12345";  
            
               Class.forName("com.mysql.jdbc.Driver").newInstance();
               con = DriverManager.getConnection(url,usuario,contraseña);
               if (con!= null)
                   System.out.println("Se ha establecido una conexion a la base de datos"+"\n"+url);
               
               stmt = con.createStatement();
               ResultSet re = stmt.executeQuery("select* from progreso");
               
               modelo = new DefaultTableModel(null,titulos);
            
               while(re.next()) {
                   
                   fila[0] = re.getString("id_proyecto");
                   fila[1] = re.getString("id_tareas");
                   fila[2] = re.getString("id_participantes");
                   fila[3] = re.getString("estadotareas");
                   fila[4] = re.getString("fecha");
                   

                   
                   modelo.addRow(fila);     
               }
               
               tabla_progresop.setModel(modelo);
                TableColumn ci = tabla_progresop.getColumn("id_proyecto");
                ci.setMaxWidth(45);
                TableColumn cn = tabla_progresop.getColumn("id_tareas");
                cn.setMaxWidth(165);
                TableColumn co = tabla_progresop.getColumn("id_participantes");
                co.setMaxWidth(160);
                TableColumn cfe = tabla_progresop.getColumn("estadotareas");
                cfe.setMaxWidth(72);
                TableColumn cte = tabla_progresop.getColumn("fecha");
                cte.setMaxWidth(72);



               
        }
        catch (Exception e) {
            
            
        }
    }

