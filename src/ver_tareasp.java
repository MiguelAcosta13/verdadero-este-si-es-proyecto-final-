    public ver_tareasp() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        this.setTitle("CONSULTAS DE TAREAS");
        this.setLocation(335,200);
        this.setResizable(false);
            
        
         try {
            
            String url = "jdbc:mysql://localhost:3306/proyecto";
            String usuario = "root";
            String contraseña = "12345";  
            
               Class.forName("com.mysql.jdbc.Driver").newInstance();
               con = DriverManager.getConnection(url,usuario,contraseña);
               if (con!= null)
                   System.out.println("Se ha establecido una conexion a la base de datos"+"\n"+url);
               
               stmt = con.createStatement();
               ResultSet re = stmt.executeQuery("select * from tareas");
               
               modelo = new DefaultTableModel(null,titulos);
            
               while(re.next()) {
                   
                   fila[0] = re.getString("id_proyecto");
                   fila[1] = re.getString("id_participantes");
                   fila[2] = re.getString("nombreTarea");
                   fila[3] = re.getString("objetivo");
                   fila[4] = re.getString("fechaInicio");
                   fila[5] = re.getString("fechaTermino");
                   fila[6] = re.getString("prioridad");

                   
                   modelo.addRow(fila);     
               }
               
               tabla_tareasp.setModel(modelo);
                TableColumn ci = tabla_tareasp.getColumn("id_proyecto");
                ci.setMaxWidth(45);
                TableColumn ca = tabla_tareasp.getColumn("id_participantes");
                ci.setMaxWidth(45);
                TableColumn cn = tabla_tareasp.getColumn("nombreTarea");
                cn.setMaxWidth(165);
                TableColumn co = tabla_tareasp.getColumn("objetivo");
                co.setMaxWidth(160);
                TableColumn cfe = tabla_tareasp.getColumn("fechaInicio");
                cfe.setMaxWidth(72);
                TableColumn cte = tabla_tareasp.getColumn("fechaTermino");
                cte.setMaxWidth(72);
                TableColumn cp = tabla_tareasp.getColumn("Prioridad");
                cp.setMaxWidth(72);


               
        }
        catch (Exception e) {
            
            
        }
        
    }

